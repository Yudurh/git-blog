public class ex18 {
    public static void main(String[] args) {

        //비교연산자
        // A == B : 두 값이 같은가? 참이면 true, 거짓이면 false 출력
        // A != B : 두 값이 같지 않은가? 참이면 true, 거짓이면 false 출력
        //  A < B : A < B 왼쪽이 작은가? 참이면 true, 아니면 false
        //  A > B : A > B 왼쪽이 큰가? 참이면 true, 아니면 false
        // A <= B : '' 작거나 같은가? 참이면 true 거짓이면 false
        // A >= B : '' 크거나 같은가? 참이면 true 거짓이면 false
        // A =< B, A => B 이런거는 없음 무조건 부등호 먼저


        int i = 10;
        int j = 20;

        System.out.println( i == j );
        System.out.println( i != j );
        System.out.println( i < j );
        System.out.println( i > j );
        System.out.println( i <= j );
        System.out.println( i >= j );














    }
}
